// Ternary Operator in JavaScript
// The ternary operator in JavaScript provides a concise way to write conditional expressions. It takes three operands: a condition followed by a question mark (?), a value to be returned if the condition is true, and a value to be returned if the condition is false.
// condition ? expression1 : expression2

const number = 7
console.log("result =", (number % 2 === 0) ? "even" : "odd")
