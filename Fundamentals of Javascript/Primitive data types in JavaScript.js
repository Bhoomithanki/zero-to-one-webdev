// Numbers :The number data type represents numeric values. It can include integers, floating-point numbers,and special values like NaN (Not-a-Number) and Infinity.

let value1 = 2 //integers
let value2 = 7.5 //Floating-point
let value3 = NaN //Not-a-number
let value4 = Infinity //infinity 
let sum = value1 + value2
console.log(sum)
console.log(value3)
console.log(value4)


//string :The string data type represents sequences of characters enclosed in single (' '), double (" "), or backticks (` `)quotes. Strings are used to represent text data.

let string1 = "Bhoomi"
let string2 = "Thanki"
let fullstring = string1 + string2
console.log(fullstring)

