// Different operatotrs in JavaScript


// uniary Operator:These operate on a single operand.
let number = 10
let number1 = -number // Unary minus operator
console.log(number1) // Output: -10

// Binary Operators:These operate on two operands.
let num1 = 5
let num2 = 3
let sum = num1 + num2 // Binary addition operator
console.log(sum) // Output: 8

// Ternary Operator:This is the only ternary operator in JavaScript. It takes three operands and returns a value based on a condition.
let age = 20;
let status = (age >= 18) ? 'Adult' : 'Minor'; // Ternary operator
console.log(status); // Output: 'Adult'
